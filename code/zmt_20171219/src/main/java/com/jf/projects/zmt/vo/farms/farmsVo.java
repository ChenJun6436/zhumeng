package com.jf.projects.zmt.vo.farms;

public class farmsVo {
	private String id;
	private String department;
	private String role;
	private String account;
	private String userName;
	private String phone;
	private String farmType;
	private String address;
	private String fooderName;
	private String status;
	/**
	 * 省id
	 */
	private Integer provinceId;
	/* 市id */
	private Integer cityId;
	/* 区县id */
	private Integer districtId;
	/* 乡镇id */
	private Integer townId;
	/* 村id */
	private Integer villageId;

	public farmsVo() {
		super();
	}

	public farmsVo(String id, String department, String role, String account,
			String userName, String phone, String farmType, String address,
			String fooderName, String status) {
		super();
		this.id = id;
		this.department = department;
		this.role = role;
		this.account = account;
		this.userName = userName;
		this.phone = phone;
		this.farmType = farmType;
		this.address = address;
		this.fooderName = fooderName;
		this.status = status;
	}

	public Integer getProvinceId() {
		return provinceId;
	}

	public void setProvinceId(Integer provinceId) {
		this.provinceId = provinceId;
	}

	public Integer getCityId() {
		return cityId;
	}

	public void setCityId(Integer cityId) {
		this.cityId = cityId;
	}

	public Integer getDistrictId() {
		return districtId;
	}

	public void setDistrictId(Integer districtId) {
		this.districtId = districtId;
	}

	public Integer getTownId() {
		return townId;
	}

	public void setTownId(Integer townId) {
		this.townId = townId;
	}

	public Integer getVillageId() {
		return villageId;
	}

	public void setVillageId(Integer villageId) {
		this.villageId = villageId;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getFarmType() {
		return farmType;
	}

	public void setFarmType(String farmType) {
		this.farmType = farmType;
	}

	public String getFooderName() {
		return fooderName;
	}

	public void setFooderName(String fooderName) {
		this.fooderName = fooderName;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
