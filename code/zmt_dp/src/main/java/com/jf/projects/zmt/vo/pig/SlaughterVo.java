package com.jf.projects.zmt.vo.pig;

/**
 * 屠宰场屠宰数量（头）
 * @author dqh
 *
 */
public class SlaughterVo {

	/* 屠宰场名称 */
	private String slaughterName;
	
	/* 屠宰数量 */
	private String pigNum;

	public String getSlaughterName() {
		return slaughterName;
	}

	public void setSlaughterName(String slaughterName) {
		this.slaughterName = slaughterName;
	}

	public String getPigNum() {
		return pigNum;
	}

	public void setPigNum(String pigNum) {
		this.pigNum = pigNum;
	}
	
}
