package com.jf.projects.zmt.vo.pig;

import java.util.List;

/**
 * 近1年生猪总数与屠宰数折线图
 * @author dqh
 *
 */
public class YearCountNumVO {
	
	/* 屠宰场数量 */
	private String slaughterHouse;

	/* 屠宰榜首（年） */
	private String slaughterChampion;
	
	/* 屠宰数 */
	private String slaughterNum;
	
	/* 折线图数据 */
	private List<CountNumVo> monthList;

	public String getSlaughterHouse() {
		return slaughterHouse;
	}

	public void setSlaughterHouse(String slaughterHouse) {
		this.slaughterHouse = slaughterHouse;
	}

	public String getSlaughterChampion() {
		return slaughterChampion;
	}

	public void setSlaughterChampion(String slaughterChampion) {
		this.slaughterChampion = slaughterChampion;
	}

	public String getSlaughterNum() {
		return slaughterNum;
	}

	public void setSlaughterNum(String slaughterNum) {
		this.slaughterNum = slaughterNum;
	}

	public List<CountNumVo> getMonthList() {
		return monthList;
	}

	public void setMonthList(List<CountNumVo> monthList) {
		this.monthList = monthList;
	}
}
