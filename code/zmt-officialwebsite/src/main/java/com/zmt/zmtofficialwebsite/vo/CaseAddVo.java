package com.zmt.zmtofficialwebsite.vo;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

/**
 * 描述: 案例vo
 *
 * @author yt
 * @create 2018-01-09 15:12
 */
public class CaseAddVo {

    /**
     * id
     */
    private Long id;

    /**
     * 案例名
     */
    private String name;

    /**
     * 发布时间
     */
    @JsonFormat(pattern = "yyyy-MM-dd HH:ss:mm", timezone = "GMT+8")
    private Date createTime;

    /**
     * 内容
     */
    private String content;

    /**
     * 类型id
     */
    private Long aanvraagTypeId;

    /**
     * 封面
     */

    private String imgUrl;

    public CaseAddVo() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Long getAanvraagTypeId() {
        return aanvraagTypeId;
    }

    public void setAanvraagTypeId(Long aanvraagTypeId) {
        this.aanvraagTypeId = aanvraagTypeId;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

}
