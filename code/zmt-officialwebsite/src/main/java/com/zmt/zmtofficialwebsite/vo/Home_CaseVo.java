public class Home_CaseVo {

  private Long id;

  private String name;

  private String imgUrl;

  public Home_CaseVo(Long id, String name, String imgUrl) {
    this.id = id;
    this.name = name;
    this.imgUrl = imgUrl;
  }

  public Home_CaseVo() {
    super();
  }

  public Long getId() {
    return id;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getImgUrl() {
    return imgUrl;
  }

  public void setImgUrl(String imgUrl) {
    this.imgUrl = imgUrl;
  }
}
