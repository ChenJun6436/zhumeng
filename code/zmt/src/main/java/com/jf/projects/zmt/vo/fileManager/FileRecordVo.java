package com.jf.projects.zmt.vo.fileManager;

public class FileRecordVo {
	private String id;
	private String sign;
	private String pigType;
	private String bulider;
	private Integer statusType;
	private String createTime;

	public FileRecordVo() {
		super();
	}

	public FileRecordVo(String id, String sign, String pigType, String bulider,
			Integer statusType, String createTime) {
		super();
		this.id = id;
		this.sign = sign;
		this.pigType = pigType;
		this.bulider = bulider;
		this.statusType = statusType;
		this.createTime = createTime;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getPigType() {
		return pigType;
	}

	public void setPigType(String pigType) {
		this.pigType = pigType;
	}

	public String getBulider() {
		return bulider;
	}

	public void setBulider(String bulider) {
		this.bulider = bulider;
	}

	public Integer getStatusType() {
		return statusType;
	}

	public void setStatusType(Integer statusType) {
		this.statusType = statusType;
	}
}
