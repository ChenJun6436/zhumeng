package com.jf.projects.zmt.vo.home;

import com.jf.projects.zmt.vo.RequestPageParam;

/**
 * 首页列表搜索条件
 * @author dev
 *
 */
public class HomeSearchVo extends RequestPageParam {
	/* 区域id */
	private Integer areaId;
	/* 层级类型 1：省  2：市  3：区县  4：乡镇  5：村 */
	private Integer type;
	/* 品种id */
	private String breedId;
	/* 状态 */
	private Integer status;
	/* 养殖场Id */
	private String farmId;
	/* 屠宰场Id */
	private String slaughterId;
	/* 销售市场Id */
	private String marketId;
	/* 关键字 */
	private String keywords;
	
	public String getKeywords() {
		return keywords;
	}
	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}
	public Integer getAreaId() {
		return areaId;
	}
	public void setAreaId(Integer areaId) {
		this.areaId = areaId;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public String getBreedId() {
		return breedId;
	}
	public void setBreedId(String breedId) {
		this.breedId = breedId;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getFarmId() {
		return farmId;
	}
	public void setFarmId(String farmId) {
		this.farmId = farmId;
	}
	public String getSlaughterId() {
		return slaughterId;
	}
	public void setSlaughterId(String slaughterId) {
		this.slaughterId = slaughterId;
	}
	public String getMarketId() {
		return marketId;
	}
	public void setMarketId(String marketId) {
		this.marketId = marketId;
	}
}
